function toggle1() {
    var x = document.getElementById("setup");

    if(x.style.display === "none") {
        x.style.display = "block";
    }

    else {
        x.style.display = "block";
    }

    var x = document.getElementById("änderungen");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }

    var x = document.getElementById("branch");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }

    var x = document.getElementById("prüfen");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }

    var x = document.getElementById("publish");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }

    var x = document.getElementById("pfade");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }

    var x = document.getElementById("intro");
    
    if(x.style.display === "block") {
        x.style.display = "none";
    }
    
    else {
        x.style.display = "none";
    }
}

